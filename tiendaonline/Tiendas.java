    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
    try {
        int codigo = Integer.parseInt(lblIdCodigo.getText());
        String nameProducto = txtProducto.getText();
        int cantidad = Integer.parseInt(txtCantidad.getText());
        double precio = Double.parseDouble(txtPrecio.getText());
        boolean stock = jCheckBoxStock.isSelected();
        
        Tienda tienda = new Tienda(nameProducto, codigo, cantidad, precio, stock);
        tienda.GuardarDatosTienda();
        JOptionPane.showMessageDialog(null, "Registro guardado!!");
        
        // Borrar los campos después de guardar los datos
        lblIdCodigo.setText("");
        txtProducto.setText("");
        txtCantidad.setText("");
        txtPrecio.setText("");
        jCheckBoxStock.setSelected(false);
        
        ConsultarTiendas(); // Actualizar la tabla después de guardar
    } catch (NumberFormatException ex) {
        // Manejar el caso en el que algún campo no tenga un valor numérico válido
        JOptionPane.showMessageDialog(null, "Error: Los campos de cantidad y precio deben contener valores numéricos válidos.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnGrabarActionPerformed

    private void tablatiendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablatiendaMouseClicked
    int fila = tablatienda.getSelectedRow();
    DefaultTableModel model = (DefaultTableModel) tablatienda.getModel();
    
    lblIdCodigo.setText(model.getValueAt(fila, 0).toString());
    txtProducto.setText(model.getValueAt(fila, 1).toString());
    txtCantidad.setText(model.getValueAt(fila, 2).toString());
    txtPrecio.setText(model.getValueAt(fila, 3).toString());
    
    // Ajustar el valor del checkbox de acuerdo al valor en la tabla
    String stockValue = model.getValueAt(fila, 4).toString();
    if (stockValue.equalsIgnoreCase("true")) {
        jCheckBoxStock.setSelected(true);
    } else {
        jCheckBoxStock.setSelected(false);
    }

    }//GEN-LAST:event_tablatiendaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    int codigo = Integer.parseInt(lblIdCodigo.getText());
    
    Tienda tienda = new Tienda();
    tienda.setCodigo(codigo); // Establecer solo el código
    
    tienda.EliminarDatosTienda();
    JOptionPane.showMessageDialog(null, "Registro eliminado!!");
    
    // Limpiar los campos después de eliminar el registro
    lblIdCodigo.setText("");
    txtProducto.setText("");
    txtCantidad.setText("");
    txtPrecio.setText("");
    jCheckBoxStock.setSelected(false);
    
    // Consultar los datos nuevamente y actualizar la tabla
    ConsultarTiendas();
    }//GEN-LAST:event_btnEliminarActionPerformed

