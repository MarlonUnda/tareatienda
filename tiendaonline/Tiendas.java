public void LimpiarTabla() {
    DefaultTableModel model = (DefaultTableModel) tablatienda.getModel();
    int rowCount = model.getRowCount();
    // Remove rows one by one from the end of the table
    for (int i = rowCount - 1; i >= 0; i--) {
        model.removeRow(i);
    }
}

    public void ConsultarTiendas() {
        LimpiarTabla(); // Método para limpiar la tabla
        DefaultTableModel model = (DefaultTableModel) tablatienda.getModel();
        try {
            // Obtener la lista de camisas desde la base de datos
            Tienda tienda = new Tienda();
            ArrayList<Tienda> listaTienda = tienda.ObtenerTienda();

            // Iterar sobre la lista de camisas y agregar cada una a la tabla
            for (Tienda tiendas : listaTienda) {
                model.addRow(new Object[]{tiendas.getCodigo(), tiendas.getProducto(), tiendas.getCantidad(), tiendas.getPrecio(), tiendas.isStock()});
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al consultar tiendas: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Cerrar recursos si es necesario
        }
    }



